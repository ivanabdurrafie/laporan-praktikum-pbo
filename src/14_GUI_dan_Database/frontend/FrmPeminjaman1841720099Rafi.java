    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        Peminjaman1841720099Rafi kat = new Peminjaman1841720099Rafi();
        kat.setmIdpeminjaman(Integer.parseInt(txtIdPeminjaman.getText()));
        kat.setmIdanggota(Integer.parseInt(txtidAnggota.getText()));
        kat.setmIdbuku(Integer.parseInt(txtidBuku.getText()));
        kat.setmTanggalPinjam(txttglpinjam.getText());
        kat.setmTanggalKembali(txttanggalkembali.getText());
        kat.saveRafi();
        txtIdPeminjaman.setText(Integer.toString(kat.getmIdpeminjaman()));
        txtidAnggota.setText(Integer.toString(kat.getmIdanggota()));
        txtidBuku.setText(Integer.toString(kat.getmIdbuku()));
        tampilkanDataRafi();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        DefaultTableModel model = (DefaultTableModel)tblKategori.getModel();
        int row = tblKategori.getSelectedRow();
        
        Peminjaman1841720099Rafi kat = new Peminjaman1841720099Rafi().getByIdRafi(Integer.parseInt(model.getValueAt(row, 0).toString()));
        kat.deleteRafi();
        kosongkanFormRafi();
        tampilkanDataRafi();
    }//GEN-LAST:event_btnHapusActionPerformed

