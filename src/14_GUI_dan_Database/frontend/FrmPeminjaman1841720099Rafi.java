    public void tampilkanDataRafi(){
        String[] kolom = {"ID","Anggota","Judul","Tanggal Pinjam","Tanggal Kembali"};
        ArrayList<Peminjaman1841720099Rafi> list = new Peminjaman1841720099Rafi().getAllRafi();
        Object rowData[] = new Object[5];
        
        tblKategori.setModel(new DefaultTableModel(new Object[][] {},kolom));
        
        for (Peminjaman1841720099Rafi kat : list){
            rowData[0] = kat.getmIdpeminjaman();
            rowData[1] = kat.getmAnggota().getmNamaRafi();
            rowData[2] = kat.getmBuku().getmJudul();
            rowData[3] = kat.getmTanggalPinjam();
            rowData[4] = kat.getmTanggalKembali();
            
            ((DefaultTableModel)tblKategori.getModel()).addRow(rowData);
        }
    }
    
